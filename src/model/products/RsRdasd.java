package model.products;

public class RsRdasd {

	public static void main(String[] args) {
		int a=2;
		int b=3;
		
		if(a==3 && b==3){
			System.out.println("Primer print B");
		}
		
		if(a==3 & b==3){
			System.out.println("Segundo Print B");
		}
		
		if(a==3 || b==3){
			System.out.println("Tercer print B");
		}
		
		if(a==3 | b==3){
			System.out.println(" cuento print B");
		}
		
		
	}

}
